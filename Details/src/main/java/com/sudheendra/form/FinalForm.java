package com.sudheendra.form;

public class FinalForm {
	private String fn;
	private String ce;
	private String ectc;
	private String cctc;
	private String np;
	public String getFn() {
		return fn;
	}
	public void setFn(String fn) {
		this.fn = fn;
	}
	public String getCe() {
		return ce;
	}
	public void setCe(String ce) {
		this.ce = ce;
	}
	public String getEctc() {
		return ectc;
	}
	public void setEctc(String ectc) {
		this.ectc = ectc;
	}
	public String getCctc() {
		return cctc;
	}
	public void setCctc(String cctc) {
		this.cctc = cctc;
	}
	public String getNp() {
		return np;
	}
	public void setNp(String np) {
		this.np = np;
	}
}
