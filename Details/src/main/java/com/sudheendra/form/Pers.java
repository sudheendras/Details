package com.sudheendra.form;

public class Pers {
	private String fn;
	private String ln;
	private String ffn;
	private String mfn;
	private String pno;
	private String email;
	public String getFn() {
		return fn;
	}
	public void setFn(String fn) {
		this.fn = fn;
	}
	public String getLn() {
		return ln;
	}
	public void setLn(String ln) {
		this.ln = ln;
	}
	public String getFfn() {
		return ffn;
	}
	public void setFfn(String ffn) {
		this.ffn = ffn;
	}
	public String getMfn() {
		return mfn;
	}
	public void setMfn(String mfn) {
		this.mfn = mfn;
	}
	public String getPno() {
		return pno;
	}
	public void setPno(String pno) {
		this.pno = pno;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
}
